# i)
x <- 2.7/2 #division
print(x) # 1.35
# ii)
x <- 2.7%/%2 #quotient
print(x) # 1
x <- 2.7%%2 #remainder
print(x) # 0.7
# iii)
x <- 10+5i/2 #imaginary number doesn't get affected, only real number gets divided
print(x) # 10+2.5i
# iv)
x <- round(2.5) #rounding off
print(x) # 2
# v)
x <- round(-2.5) #rounding off
print(x) # -2
# vi)
x <- 2%/%4-1 # '%/%' has more precedence than '-'
print(x) # -1
# vii) 
x <- 3*2**2 # '**' has more precedence than '*'
print(x) # 12
x <- 3*2^2 #similar to above
print(x) # 12
# viii) 
x <- 3**2*2 # '**' has more precedence than '*'
print(x) # 18
# ix)
x <- 7%/%4 # quotient
print(x) # 1
# x) 
x <- 7%%4 #remainder
print(x) # 3
# xi)
x <- -7%%4 #remainder
print(x) # 1
# xii)
x <- trunc(5.7) #removes the fractional part
print(x) # 5
# xiii)
x <- trunc(-5.7) #removes the fractional part
print(x) # -5
